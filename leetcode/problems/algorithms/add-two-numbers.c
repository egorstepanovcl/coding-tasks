// Название: 2. Сложить два числа (2. Add Two Numbers)

// Задача:
// Вам даны два непустых связанных списка, представляющих два неотрицательных целых числа. Цифры хранятся в обратном порядке, и каждый из их узлов содержит одну цифру. Сложите эти два числа и верните сумму в виде связанного списка.
// Можно предположить, что эти два числа не содержат никаких ведущих нулей, кроме самого числа 0.

// Пример 1:
// Вход: l1 = [2,4,3], l2 = [5,6,4]
// Выход: [7,0,8]
// Пояснение: 342 + 465 = 807.

// Пример 2:
// Вход: l1 = [0], l2 = [0]
// Выход: [0]

// Пример 3:
// Вход: l1 = [9,9,9,9,9,9,9,9], l2 = [9,9,9,9]
// Выход: [8,9,9,9,0,0,0,1]

// Ограничения:
// Количество узлов в каждом связанном списке находится в диапазоне [1, 100].
// 0 <= Node.val <= 9
// Гарантируется, что список представляет число, не содержащее ведущих нулей.

#include <stdlib.h>

// Функция для сложения двух связанных списков
struct ListNode* addTwoNumbers(struct ListNode* l1, struct ListNode* l2) {
    // Создаем фиктивный узел для упрощения работы с указателями
    struct ListNode dummy;
    struct ListNode* tail = &dummy;
    tail->next = NULL;

    int carry = 0;  // Переменная для хранения переноса
    while (l1 != NULL || l2 != NULL || carry != 0) {
        int sum = carry;  // Начинаем с переноса

        if (l1 != NULL) {
            sum += l1->val;  // Добавляем значение текущего узла l1
            l1 = l1->next;  // Переходим к следующему узлу
        }

        if (l2 != NULL) {
            sum += l2->val;  // Добавляем значение текущего узла l2
            l2 = l2->next;  // Переходим к следующему узлу
        }

        carry = sum / 10;  // Перенос для следующего разряда
        sum = sum % 10;  // Оставляем только текущий разряд

        // Создаем новый узел для результата
        struct ListNode* newNode = (struct ListNode*)malloc(sizeof(struct ListNode));
        newNode->val = sum;
        newNode->next = NULL;

        tail->next = newNode;  // Присоединяем новый узел к результату
        tail = newNode;  // Обновляем указатель на конец списка
    }

    return dummy.next;  // Возвращаем начало списка (пропускаем фиктивный узел)
}
// I        1
// V        5
// X        10
// L        50
// C        100
// D        500
// M        1000
// Римские цифры образуются путем добавления преобразований десятичных значений от наибольшего к наименьшему. Преобразование десятичного значения в римскую цифру происходит по следующим правилам:
// Если значение не начинается с 4 или 9, выберите символ максимального значения, которое можно вычесть из исходного, добавьте этот символ к результату, вычтите его значение и преобразуйте остаток в римскую цифру.
// Если значение начинается с 4 или 9, используйте форму вычитания, представляющую собой один символ, вычитаемый из следующего символа, например, 4 на 1 (I) меньше, чем 5 (V): IV, а 9 - на 1 (I) меньше 10 (X): IX. Используются только следующие вычитательные формы: 4 (IV), 9 (IX), 40 (XL), 90 (XC), 400 (CD) и 900 (CM).
// Только силы 10 (I, X, C, M) могут быть добавлены последовательно не более 3 раз для обозначения кратных 10. Вы не можете добавлять 5 (V), 50 (L) или 500 (D) несколько раз. Если вам нужно добавить символ 4 раза, используйте вычитательную форму.
// Задав целое число, преобразуйте его в римскую цифру.

// Пример 1:
// Вход: num = 3749
// Выход: "MMMDCCXLIX"
// Пояснения:
// 3000 = MMM as 1000 (M) + 1000 (M) + 1000 (M)
//  700 = DCC as 500 (D) + 100 (C) + 100 (C)
//   40 = XL as 10 (X) less of 50 (L)
//    9 = IX as 1 (I) less of 10 (X)
// Примечание: 49 не на 1 (I) меньше 50 (L), потому что преобразование основано на десятичных знаках.

// Пример 2:
// Вход: num = 58
// Выход: "LVIII"
// Пояснение:
// 50 = L
//  8 = VIII

// Пример 3:
// Вход: num = 1994
// Выход: "MCMXCIV"
// Пояснение:
// 1000 = M
//  900 = CM
//   90 = XC
//    4 = IV

// Ограничения:
// 1 <= num <= 3999

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// Функция для конвертации числа в римское число
char* intToRoman(int num) {
    // Массивы римских символов и соответствующих значений
    char *romanSymbols[] = {"M", "CM", "D", "CD", "C", "XC", "L", "XL", "X", "IX", "V", "IV", "I"};
    int romanValues[] = {1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1};
    
    // Максимальная длина римского числа для диапазона от 1 до 3999 — 15 символов
    char *result = (char*)malloc(16 * sizeof(char));
    result[0] = '\0'; // Инициализация пустой строки
    
    // Проходим по всем римским значениям
    for (int i = 0; i < 13; i++) {
        // Пока число больше или равно текущему римскому значению
        while (num >= romanValues[i]) {
            // Добавляем соответствующий римский символ к результату
            strcat(result, romanSymbols[i]);
            // Вычитаем римское значение из числа
            num -= romanValues[i];
        }
    }
    
    return result;
}
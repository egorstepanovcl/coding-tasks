// Название: 5. Самая длинная палиндромная подстрока (5. Longest Palindromic Substring)

// Задача:
// Если задана строка s, верните самую длинную палиндромную подстроку в s.

// Пример 1:
// Вход: s = «babad»
// Выход: «bab»
// Пояснение: «aba» также является правильным ответом.

// Пример 2:
// Вход: s = «cbbd»
// Выход: «bb»

// Ограничения:
// 1 <= s.length <= 1000
// s состоит только из цифр и английских букв.

#include <string.h>

// Функция для нахождения наибольшей палиндромной подстроки
char* longestPalindrome(char* s) {
    int len = strlen(s);
    if (len == 0) return ""; // Если строка пустая, возвращаем пустую строку
    
    int start = 0; // Начальная позиция палиндрома
    int maxLength = 1; // Максимальная длина палиндрома

    // Динамическое программирование для нахождения палиндрома
    int dp[1000][1000] = {0}; // dp[i][j] указывает, является ли подстрока s[i..j] палиндромом

    // Все подстроки длиной 1 являются палиндромами
    for (int i = 0; i < len; i++) {
        dp[i][i] = 1;
    }

    // Проверяем подстроки длиной 2
    for (int i = 0; i < len - 1; i++) {
        if (s[i] == s[i + 1]) {
            dp[i][i + 1] = 1;
            start = i;
            maxLength = 2;
        }
    }

    // Проверяем подстроки длиной больше 2
    for (int k = 3; k <= len; k++) {
        for (int i = 0; i < len - k + 1; i++) {
            int j = i + k - 1; // Конечная позиция подстроки

            // Если крайние символы равны и внутренняя подстрока является палиндромом
            if (s[i] == s[j] && dp[i + 1][j - 1]) {
                dp[i][j] = 1;
                if (k > maxLength) {
                    start = i;
                    maxLength = k;
                }
            }
        }
    }

    // Выделяем память для результата
    char* result = (char*)malloc((maxLength + 1) * sizeof(char));
    strncpy(result, s + start, maxLength);
    result[maxLength] = '\0'; // Добавляем завершающий ноль

    return result;
}
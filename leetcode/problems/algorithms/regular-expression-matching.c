// Задача:
// Учитывая входную строку s и шаблон p, реализуйте сопоставление регулярных выражений с поддержкой '.' и '*', где:
// '.' Сопоставляет любой одиночный символ.
// '*' Совпадает с нулем или более из предыдущего элемента.
// Соответствие должно охватывать всю входную строку (не частично).

// Пример 1:
// Вход: s = "aa", p = "a"
// Выход: false
// Пояснение: "a" не соответствует всей строке "aa".

// Пример 2:
// Вход: s = "aa", p = "a*"
// Выход: true
// Пояснение: '*' означает ноль или более предшествующих элементов, 'a'. Поэтому, повторив 'a' один раз, он превращается в "aa".

// Пример 3:
// Вход: s = "ab", p = ".*"
// Выход: true
// Пояснения: ".*" означает "ноль или более (*) любых символов (.)".

// Ограничения:
// 1 <= s.length <= 20
// 1 <= p.length <= 20
// s содержит только строчные английские буквы.
// p содержит только строчные английские буквы, '.' и '*'.
// Гарантируется, что для каждого появления символа '*' будет найден предыдущий допустимый символ.

#include <stdbool.h>
#include <string.h>

// Функция для проверки регулярного выражения
bool isMatch(char *s, char *p) {
    int m = strlen(s);
    int n = strlen(p);

    // Динамическая таблица dp[m+1][n+1]
    bool dp[m+1][n+1];
    
    // Инициализация всех ячеек таблицы как false
    memset(dp, false, sizeof(dp));

    // Пустая строка и пустой паттерн совпадают
    dp[0][0] = true;

    // Инициализация первой строки для случаев, когда паттерн содержит '*' вначале
    for (int j = 1; j <= n; j++) {
        if (p[j-1] == '*') {
            dp[0][j] = dp[0][j-2];
        }
    }

    // Основной цикл по строке и паттерну
    for (int i = 1; i <= m; i++) {
        for (int j = 1; j <= n; j++) {
            if (p[j-1] == '.' || p[j-1] == s[i-1]) {
                // Если символы совпадают или '.' - то берем предыдущее совпадение
                dp[i][j] = dp[i-1][j-1];
            } else if (p[j-1] == '*') {
                // Если '*', то проверяем два случая:
                // 1. '*' означает 0 вхождений предыдущего символа
                dp[i][j] = dp[i][j-2];

                // 2. '*' означает одно или более вхождений предыдущего символа
                if (p[j-2] == '.' || p[j-2] == s[i-1]) {
                    dp[i][j] = dp[i][j] || dp[i-1][j];
                }
            }
        }
    }

    // Результат в правом нижнем углу таблицы
    return dp[m][n];
}
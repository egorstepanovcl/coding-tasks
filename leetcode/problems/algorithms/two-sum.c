// Название: 1. Две суммы (1. Two Sum)

// Задача:
// Учитывая массив целых чисел nums и целое число target, верните индексы этих двух чисел так, чтобы их сумма равнялась target.
// Вы можете предположить, что каждый вход будет иметь ровно одно решение, и не можете использовать один и тот же элемент дважды.
// Вы можете возвращать ответы в любом порядке.

// Пример 1:
// Вход: nums = [2,7,11,15], цель = 9
// Выход: [0,1]
// Пояснения: Поскольку nums[0] + nums[1] == 9, мы возвращаем [0, 1].

// Пример 2:
// Вход: nums = [3,2,4], target = 6
// Выход: [1,2]

// Пример 3:
// Вход: nums = [3,3], цель = 6
// Выход: [0,1]

// Ограничения:
// 2 <= nums.length <= 10⁴
// -10⁹ <= nums[i] <= 10⁹
// -10⁹ <= target <= 10⁹
// Существует только один правильный ответ.

#include <stdlib.h>

int* twoSum(int* nums, int numsSize, int target, int* returnSize) {
    // Выделяем память под массив, в котором будем хранить индексы
    int* result = (int*)malloc(2 * sizeof(int));
    *returnSize = 2;  // Возвращаемый массив всегда будет содержать два элемента

    // Проходим по каждому элементу массива
    for (int i = 0; i < numsSize - 1; i++) {
        // Проверяем все элементы, которые идут после текущего
        for (int j = i + 1; j < numsSize; j++) {
            // Если сумма двух чисел равна целевому значению, возвращаем их индексы
            if (nums[i] + nums[j] == target) {
                result[0] = i;
                result[1] = j;
                return result;
            }
        }
    }

    // Если решение не найдено, возвращаем NULL (хотя по условию задачи, всегда есть одно решение)
    *returnSize = 0;
    return NULL;
}